    private void switchView(){
        boolean isVisible = kundenPanel.isVisible();
        kundenPanel.setVisible(!isVisible);
        lieferantenPanel.setVisible(isVisible);
        
        if(kundenPanel.isVisible()){
            strassekField.setText(strasselField.getText());
            nummerkField.setText(nummerlField.getText());
            ortkField.setText(ortlField.getText());
            plzkField.setText(plzlField.getText());
            ibankField.setText(ibanlField.getText());
            emailkField.setText(emaillField.getText());
                    
        }else{
            strasselField.setText(strassekField.getText());
            nummerlField.setText(nummerkField.getText());
            ortlField.setText(ortkField.getText());
            plzlField.setText(plzkField.getText());
            ibanlField.setText(ibankField.getText());
            emaillField.setText(emailkField.getText());
        }
    }
    
