    private void createKundeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createKundeButtonActionPerformed
        
        switch(evt.getActionCommand()){
            case CLOSE_ACTION_COMMAND:
                this.dispose();
                break;
            default:
                String strasse  = strassekField.getText();
                String nummer   = nummerkField.getText();
                String ort      = ortkField.getText();
                int plz         = 0;
                String iban     = ibankField.getText();
                String email    = emailkField.getText();
                String vorname  = vornameField.getText();
                String nachname = nachnameField.getText();

                try{
                    plz = Integer.parseInt(plzkField.getText());
                }catch(NumberFormatException ex){}

                geschaeftspartner = new Kunde(null, strasse,nummer,ort, plz,iban,email,vorname,nachname);
                this.dispose();
        }
    }//GEN-LAST:event_createKundeButtonActionPerformed

